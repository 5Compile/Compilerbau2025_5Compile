public class CompAssign {
    public int increase(int a) {
        a += 1;
        return a;
    }

    public int decrease(int a) {
        a -= 1;
        return a;
    }

    public int multiply(int a) {
        a *= 2;
        return a;
    }

    public int divide(int a) {
        a /= 2;
        return a;
    }

    public int modulus(int a) {
        a %= 2;
        return a;
    }
}